def Connected_Component(a,x,y): 
    if a[x][y] != 1 and x <= len(a) and y <= len(a[0]):
        a[x][y] = 1
        # print(arr)
        Connected_Component(a,x,y+1)
        Connected_Component(a,x+1,y)
        Connected_Component(a,x,y-1)
        Connected_Component(a,x-1,y)
    else:
        return(a)
    return(a)

